# function to verify that task is correct in Lesson , Exercise , Task "
def verify(lesson, exercise, task, answer):
    LES = Course[lesson]
    EX = LES[exercise]
    TASK = EX[task]
    if TASK == answer:
        status = 'CORRECT'
    else:
        status = 'INCORRECT'
    return status

