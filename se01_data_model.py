# function to return 3 nouns that are different from the nouns in list_NO
def give_noons(list_NO):
    given_NO = []
    # Get all words that are :
    #   - Nouns
    #   - not in list_NO
    for j in range(len(Words)):
        if Words[j][1] == 'NO' and Words[j][0] not in list_NO:
            given_NO.append(Words[j][0])
    # Retrieve 3 random samples from list
    given_NO = random.sample(given_NO, 3)
    return given_NO

list = ['kläder', 'Kvinna']
given = give_noons(list)
print(given)

